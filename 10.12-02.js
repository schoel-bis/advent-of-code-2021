const PAIRS = new Map([
    ['(', ')'],
    ['[', ']'],
    ['{', '}'],
    ['<', '>']
]);

const SCORES = new Map([
    [')', 1],
    [']', 2],
    ['}', 3],
    ['>', 4]
]);

const lines = input
    .split('\n')
    .map(l => l.trim())
    .filter(x => x);

function getCompletion(line) {
    const openCommands = [];

    for (let i = 0; i < line.length; i++) {
        const c = line[i];

        if (PAIRS.has(c)) {
            openCommands.push(c);
        } else {
            const matchingCommand = openCommands.pop();
            if (PAIRS.get(matchingCommand) !== c) {
                return null;
            }
        }
    }

    return openCommands
        .reverse()
        .map(c => PAIRS.get(c));
}

function calculateCompletionScore(completions) {
    return completions.reduce(
        (acc, c) => acc * 5 + SCORES.get(c),
        0
    );
}

const lineScores = lines.map(getCompletion)
    .filter(x => x)
    .map(calculateCompletionScore)
    .sort((a, b) => a - b);

